#include "rush.h"

int		is_numeric(char c)
{
	if (c >= '0' && c <= '9')
		return (TRUE);
	return (FALSE);
}

int		is_below_n(int n, char c)
{
	if (c <= n + '0' && c >= '1')
		return (1);
	return (0);
}
